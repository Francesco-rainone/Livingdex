/// Material Design 3 responsive width breakpoints.
class Breakpoints {
  /// Compact: phones portrait (<600).
  static int get compact => 600;

  /// Medium: small tablets/landscape phones (600-840).
  static int get medium => 840;

  /// Expanded: large tablets/small laptops (840-1200).
  static int get expanded => 1200;

  /// Large: desktops (1200-1600).
  static int get large => 1600;
}
